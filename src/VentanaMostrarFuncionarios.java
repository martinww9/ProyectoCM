    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        jTextArea1.setText(""); // Limpia el área de texto

        // Redirige la salida estándar a tu área de texto
        PrintStream printStream = new PrintStream(new CustomOutputStream(jTextArea1));
        System.setOut(printStream);

        // Obtiene el cargo especificado por el usuario desde un campo de texto
        String cargoBuscado = jTextField1.getText(); // Suponiendo que jTextFieldCargo es el campo de entrada del cargo
        
        // Muestra los funcionarios por el cargo en todas las carteras
        List<Funcionario> funcionariosEncontrados = sistema.buscarFuncionariosPorCargoEnTodasLasCarteras(cargoBuscado);

        if(funcionariosEncontrados.isEmpty()){
            JOptionPane.showMessageDialog(this, "Error: Cargo no valido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        System.out.println("Funcionarios por cargo: " + cargoBuscado);
        System.out.println("");
        
        for (Funcionario funcionario : funcionariosEncontrados) {
            System.out.println(funcionario.toString());
            System.out.println("");
        }

        JOptionPane.showMessageDialog(this, "Funcionarios mostrados por cargo.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton3ActionPerformed

